package day05;

public class ArrayEx2 {

	public static void main(String[] args) {
		// 자료형 [] 배열
		int []arr1;
		// 자료형 배열 []   *arr1, arr2 두개의 차이점은 없다. 한개만 되어있기 때문에
		int arr2[];
		
		//				*arr4, arr6 두개의 차이점은 있다. arr4는 ,(컴마)로 이어져 있어서 arr3이랑 같게 배열로 됨.

		//arr3 : 배열 , arr4 : 배열
		int []arr3, arr4;
		
		//arr5 : 배열 , arr6 : 변수
		int arr5[], arr6;
	}

}
